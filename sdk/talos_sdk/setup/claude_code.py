"""Write a sourceable env file to point Claude Code at the Talos gateway.

Claude Code has no single cross-version settings file for this, so we write
a small env file you source before launching `claude`, same as docs/claude-code.
"""
from __future__ import annotations

from ..config import DEFAULT_ANTHROPIC_BASE_URL, DEFAULT_MODEL
from .common import write_with_backup
from .paths import claude_code_env


def setup(
    api_key: str,
    base_url: str = DEFAULT_ANTHROPIC_BASE_URL,
    model: str = DEFAULT_MODEL,
    dry_run: bool = False,
) -> str:
    path = claude_code_env()
    content = (
        "# Generated by `talos setup claude-code`. Source this before launching claude:\n"
        f'export ANTHROPIC_BASE_URL="{base_url}"\n'
        f'export ANTHROPIC_API_KEY="{api_key}"\n'
        f'export ANTHROPIC_MODEL="{model}"\n'
        f'export ANTHROPIC_SMALL_FAST_MODEL="{model}"\n'
    )
    result = write_with_backup(path, content, dry_run=dry_run)
    return result + f"\nRun `source {path}` before launching claude (or add it to your shell profile)."
